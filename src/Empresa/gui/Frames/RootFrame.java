    private void crearHotelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearHotelButtonActionPerformed
        String nombre = this.nombreDeHotelTextField.getText();
        String direccion = this.direccionDeHotelTextField.getText();
        if (nombre.isEmpty() || direccion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Faltan campos por llenar");
            this.nombreDeHotelTextField.setText("");
            this.direccionDeHotelTextField.setText("");
        } else {
            RegistroDeEmpresas nuevoRegistro = new RegistroDeEmpresas();
            String codigoDeHotel = GeneradorDeCodigos.generarCodigo();
            Empresa nuevaEmpresa = new Empresa("Hotel", nombre, direccion, codigoDeHotel);
            try {
                BusquedaDeEmpresas nuevaBusqueda = new BusquedaDeEmpresas();
                Empresa empresaConMismaDireccion = nuevaBusqueda.buscarEmpresaConDireccion(direccion, "Hotel");
                if (empresaConMismaDireccion != null) {
                    JOptionPane.showMessageDialog(this, "El hotel con la direccion:" + direccion + " " + "ya existe");

                } else {
                    nuevoRegistro.escribirEmpresa(nuevaEmpresa);
                    this.nombreDeHotelTextField.setText("");
                    this.direccionDeHotelTextField.setText("");
                    JOptionPane.showMessageDialog(this, "Se ha creado el hotel");
                    this.hotelesActuales = nuevaBusqueda.buscarEmpresas("Hotel");
                    DefaultTableModel miModelo = (DefaultTableModel) this.hotelesCreadosTable.getModel();
                    miModelo.setRowCount(0);
                    llenarTablaDeHoteles(hotelesActuales);//Llenar la tabla de hoteles
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Existio un error al conectar a la base de datos");
                //Logger.getLogger(RootFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_crearHotelButtonActionPerformed

