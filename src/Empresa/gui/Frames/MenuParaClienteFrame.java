    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int numeroDeFila = this.habitacionesDisponiblesTable.getSelectedRow();
        if (numeroDeFila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una habitacion");
        } else {
            Habitacion hab = this.habitacionesDisponibles.get(numeroDeFila);
            this.habitacionesQueSeReservaran.add(this.habitacionesDisponibles.get(numeroDeFila));
            DefaultTableModel miModelo = (DefaultTableModel) this.habitacionesA_ReservarTable.getModel();
            //piso tipo numeroDeHabitacion CostoPorNoche Dias CosotTotal
            Double costoTotal = hab.getCostoPorNoche() * this.diasDeAlojamiento;
            miModelo.addRow(new Object[]{hab.getPiso(), hab.getTipoDeHabitacion(), hab.getNumeroDeHabitacion(), hab.getCostoPorNoche(), this.diasDeAlojamiento, costoTotal});
            this.habitacionesDisponibles.remove(numeroDeFila);
            miModelo = (DefaultTableModel) this.habitacionesDisponiblesTable.getModel();
            miModelo.removeRow(numeroDeFila);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

