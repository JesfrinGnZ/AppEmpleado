    private void buscarHabitcionesDisponiblesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarHabitcionesDisponiblesButtonActionPerformed
        Date tiempo1 = fechaInDateChooser.getDate();
        Date tiempo2 = fechaOutDateChooser.getDate();
        try {
            java.sql.Date fechaBaseDeDatos1 = new java.sql.Date(tiempo1.getTime());
            java.sql.Date fechaBaseDeDatos2 = new java.sql.Date(tiempo2.getTime());
            if (fechaBaseDeDatos1.toString().equals(fechaBaseDeDatos2.toString())) {
                JOptionPane.showMessageDialog(this, "La fecha de CheckIn no puede ser igual a la de check Out");
            } else if (fechaBaseDeDatos1.after(fechaBaseDeDatos2)) {
                JOptionPane.showMessageDialog(this, "La fecha de checkIn no puede ser mayor a la de checkOut");
            } else {
                BusquedaDeHabitaciones nuevaBusqueda = new BusquedaDeHabitaciones();
                if (this.hotelSeleccionado == null) {
                    JOptionPane.showMessageDialog(this, "Debe seleccionar un hotel");
                } else {
                    this.habitacionesDisponibles = nuevaBusqueda.buscarHabitacionesSinReservacion(fechaBaseDeDatos1, fechaBaseDeDatos2, this.hotelSeleccionado.getCodigoDeEmpresa());
                    DefaultTableModel miModelo = (DefaultTableModel) this.habitacionesDisponiblesTable.getModel();
                    for (Habitacion habitacion : this.habitacionesDisponibles) {
                        miModelo.addRow(new Object[]{habitacion.getPiso(), habitacion.getTipoDeHabitacion(), habitacion.getNumeroDeHabitacion(), habitacion.getCostoPorNoche()});
                    }
                    //Calcular dias de difirencia
                    DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    this.diasDeAlojamiento = calculrDiasDeDiferencia(dateFormat.format(tiempo1), dateFormat.format(tiempo2));
                    JOptionPane.showMessageDialog(this, diasDeAlojamiento);

                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Existio un error al conectar con la base de datos");
        }

    }//GEN-LAST:event_buscarHabitcionesDisponiblesButtonActionPerformed

