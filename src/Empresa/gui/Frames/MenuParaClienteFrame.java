    private void buscarHoteles() {
        BusquedaDeEmpresas nuevaBusqueda = new BusquedaDeEmpresas();
        DefaultTableModel miModelo = (DefaultTableModel) this.hotelesDeEmpresaTable.getModel();
        try {
            this.listaDeHoteles = nuevaBusqueda.buscarEmpresas("Hotel");
            for (Empresa hotel : listaDeHoteles) {
                miModelo.addRow(new Object[]{hotel.getNombreDeEmpresa(), hotel.getDireccionDeEmpresa()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos");
            Logger.getLogger(MenuParaClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public int calculrDiasDeDiferencia(String fechaInicio, String fechaFinal) {
        try {
            SimpleDateFormat nuevoFormato = new SimpleDateFormat("yyyy-MM-dd");
            Date fechaInicial = nuevoFormato.parse(fechaInicio);
            Date fechaFin = nuevoFormato.parse(fechaFinal);
            int dias = (int) ((fechaFin.getTime() - fechaInicial.getTime()) / 86400000);
            return dias;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Existio un error al darle formato a las fechas");
        }
        return 0;
    }

